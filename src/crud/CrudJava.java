    public void tampil() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("No Telepon");
        model.addColumn("Alamat");

        try {
            String sql = "SELECT * FROM latihan";
            KoneksiBonecp kp = new KoneksiBonecp();
            
            java.sql.Connection conn=kp.getConnection();
            java.sql.Statement pst=conn.createStatement();
            java.sql.ResultSet res=pst.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            }
            tabel1.setModel(model);
        }catch(Exception e){
        }
    }
          
        
