    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed

        try {
            if ("".equals(txt_id.getText()) || "".equals(txt_nama.getText()) || "".equals(txt_telp.getText()) || "".equals(txt_alamat.getText())){
                JOptionPane.showMessageDialog(null, "Data Belum Lengkap!");
            }
            String sql = "UPDATE latihan SET nama='"+txt_nama.getText()+"',telepon = '"+txt_telp.getText()+"',alamat = '"+txt_alamat.getText()+"' WHERE id='"+ txt_id.getText()+"'";
            java.sql.Connection conn=(Connection)koneksiDbase.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        tampil();
        kosong();
     // TODO add your handling code here:
    }//GEN-LAST:event_ubahActionPerformed

